let x: number = 10;
let y: number = 5;

console.log("Addition (x + y):", x + y);           // 15
console.log("Subtraction (x - y):", x - y);        // 5
console.log("Multiplication (x * y):", x * y);     // 50
console.log("Division (x / y):", x / y);           // 2
console.log("Modulus (x % y):", x % y);            // 0